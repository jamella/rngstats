typedef struct
{
    size_t offset;
    int x;
    int y;
    uint8_t m[256];
}
arc4_context;

static void
arc4_init(void *ctx_, const uint8_t *key)
{
    arc4_context *ctx = ctx_;
    int i, j, a;
    unsigned int k;
    uint8_t *m;

    ctx->offset = 0;
    ctx->x = 0;
    ctx->y = 0;
    m = ctx->m;

    for (i = 0; i < 256; i++)
        m[i] = (uint8_t)i;

    j = k = 0;

    for (i = 0; i < 256; i++, k++)
    {
        if (k >= 16)
            k = 0;

        a = m[i];
        j = (j + a + key[k]) & 0xFF;
        m[i] = m[j];
        m[j] = (uint8_t)a;
    }
}

#define ARC4_ROUND(x, y, a, b, m) do {          \
        x = (x + 1) & 0xFF;                     \
        a = m[x];                               \
        y = (y + a) & 0xFF;                     \
        b = m[y];                               \
                                                \
        m[x] = (uint8_t)b;                      \
        m[y] = (uint8_t)a;                      \
    } while (0);

static void
arc4_gen_keystream(void *ctx_, size_t offset, uint8_t *obuf, size_t olen)
{
    arc4_context *ctx = ctx_;
    int x, y, a, b;
    size_t i;
    uint8_t *m;

    x = ctx->x;
    y = ctx->y;
    m = ctx->m;

    if (offset < ctx->offset)
        abort();

    for (i = ctx->offset; i < offset; i++)
        ARC4_ROUND(x, y, a, b, m);

    for (i = 0; i < olen; i++)
    {
        ARC4_ROUND(x, y, a, b, m);
        obuf[i] = (uint8_t) m[(uint8_t)(a + b)];
    }

    ctx->x = x;
    ctx->y = y;
    ctx->offset = offset + olen;
}

