static void
dump_hex(const char *label, const uint8_t *p, size_t n)
{
    size_t i;
    fputs(label, stderr);
    for (i = 0; i < n; i++)
        fprintf(stderr, "%02x", (unsigned int)p[i]);
    putc('\n', stderr);
}

static void
arc4_selftest(void)
{
    int i, j;
    uint8_t ksbuf[16];
    arc4_context ctx;
    bool failed = false;

    for (i = 0; i < 2; i++)
    {
        arc4_init(&ctx, arc4_test_keys[i]);

        for (j = 0; j < 18; j++)
        {
            arc4_gen_keystream(&ctx,
                               arc4_test_keystreams[i][j].offset,
                               ksbuf, 16);

            if (memcmp(ksbuf, arc4_test_keystreams[i][j].sample, 16))
            {
                fprintf(stderr, "FAIL: arc4 keystream %d/%d (offset %d):\n",
                        i+1, j+1, arc4_test_keystreams[i][j].offset);
                dump_hex("  exp: ", arc4_test_keystreams[i][j].sample, 16);
                dump_hex("  got: ", ksbuf, 16);
                putc('\n', stderr);
                failed = true;
            }
        }
    }
    if (failed)
        abort();
}

